import { model, Schema } from 'mongoose'

const UserSchema = Schema({
  email: {
    type: String,
    required: true,
    unique: 1
  },
  password: {
    type: String,
    required: true,
    minlength: 5
  },
  name: {
    type: String,
    required: true,
    maxlength: 100
  },
  lastname: {
    type: String,
    required: true,
    maxlength: 100
  },
  cart: {
    type: Array,
    default: []
  },
  history: {
    type: Array,
    default: []
  },
  role: {
    type: Number,
    default: 0
  },
  token: {
    type: String
  },
  isAdmin: {
    type: Boolean,
    default: false
  }
})

export default model('User', UserSchema, 'Users')
